
public class CheckPoint {
	private int x,y,width,height;
	CheckPoint(int initX, int initY, int cWidth, int cHeight)
	{
		x = initX;
		y = initY;
		width = cWidth;
		height = cHeight;
	}
	public int getX()
	{
		return x;
	}
	public int getY()
	{
		return y;
	}
	public int getWidth()
	{
		return width;
	}
	public int getHeight()
	{
		return height;
	}
}
